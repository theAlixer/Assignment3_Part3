
public class DoubleCalculator {
	
	 public int doubleMultiply(int x, int y) {
			return (x * x) + (y * y);
		}

	 public int doubleAdd(int x, int y) {
		 return (x + x) + (y + y);
	 }
	 
	 public int percentage(int x, int y) {
		 int percentageCalc = ((x / y) * 100);
		 return percentageCalc;
	 }

	 public int divide(int x, int y) {
		 return x / y;
	 }


}
